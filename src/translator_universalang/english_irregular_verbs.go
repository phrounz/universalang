func loadIrregularVerbs() {
	if len(irregularVerbsInfinitive) != len(irregularVerbsPastSimple) /*|| len(irregularVerbsInfinitive) != len(irregularVerbsPastParticiple)*/ {
		panic(fmt.Sprintf("len %d %d %d", len(irregularVerbsInfinitive), len(irregularVerbsPastSimple), len(irregularVerbsPastParticiple)))
	}
	for i, infinitive := range irregularVerbsInfinitive {
		var pastSimple = irregularVerbsPastSimple[i]
		//var pastParticiple = irregularVerbsPastParticiple[i]
		constFullWordsTranslations[pastSimple] = translationSchema{wordToTranslate: infinitive, suffix: constPastTenseSuffix}
		//constFullWordsTranslations[pastParticiple] = translationSchema{wordToTranslate: infinitive, suffix: constPastParticipleSuffix}
	}
}

